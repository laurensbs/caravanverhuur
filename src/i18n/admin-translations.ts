export type AdminLocale = 'nl' | 'en';
export type AdminRole = 'admin' | 'staff';

// eslint-disable-next-line @typescript-eslint/no-explicit-any
type Dict = Record<string, any>;

function getNestedValue(obj: Dict, path: string): string {
  const keys = path.split('.');
  let current: unknown = obj;
  for (const key of keys) {
    if (current && typeof current === 'object' && key in (current as Dict)) {
      current = (current as Dict)[key];
    } else {
      return path;               // fallback: return key itself
    }
  }
  return typeof current === 'string' ? current : path;
}

export function createT(locale: AdminLocale) {
  const dict = translations[locale];
  return (key: string, params?: Record<string, string | number>): string => {
    let val = getNestedValue(dict, key);
    if (params) {
      Object.entries(params).forEach(([k, v]) => {
        val = val.replace(`{${k}}`, String(v));
      });
    }
    return val;
  };
}

